function getDate() {
    var today = new Date();
    console.log(today);
    var month;
    if(today.getMonth() + 1 == 13) {
        month = 12;
    } else {
        month = today.getMonth()+1;
    }
    var date = month+"/"+today.getDate()+"/"+today.getFullYear();
    return date;
}

function generateUniqueID(length, chars) {
    var id = '';
    for (var i = length; i > 0; --i) id += chars[Math.floor(Math.random() * chars.length)];
    return id;
}

