function search() {
    var input, filter, table, tr, td, i, txtValue;
    input = document.getElementById("search");
    filter = input.value.toUpperCase();
    table = document.getElementById("table");
    tr = table.getElementsByTagName("tr");

    for (i = 0; i < tr.length; i++) {
        td = tr[i].getElementsByTagName("td")[0];
        if (td) {
            txtValue = td.textContent || td.innerText;
            if (txtValue.toUpperCase().indexOf(filter) > -1) {
                tr[i].style.display = "";
            } else {
                tr[i].style.display = "none";
            }
        }
    }
}

function validateForm() {
    var nameField = document.getElementById('name').value;
    var phoneField = document.getElementById('phone').value;
    var emailField = document.getElementById('email').value;
    var fromField = document.getElementById('from').value;
    var toField = document.getElementById('to').value;
    var homeField = document.getElementById('home').checked;
    var businessField = document.getElementById('business').checked;
    var typeField = homeField || businessField;
    var smallTruckField = document.getElementById('size1').checked;
    var mediumTruckField = document.getElementById('size2').checked;
    var largeTruckField = document.getElementById('size3').checked;
    var truckField = smallTruckField || mediumTruckField || largeTruckField;

    if(nameField && phoneField && emailField && fromField && toField && typeField && truckField) {
        document.getElementById("add").disabled = false;
    } else {
        document.getElementById("add").disabled = true;
    }
}

